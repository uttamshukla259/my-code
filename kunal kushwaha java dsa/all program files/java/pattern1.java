public class pattern1 {
    public static void main(String[] args) {
        int m = 3;
        int n = 4;
        for (int i = 1; i <= m; i++) {
            for (int j = 1; j <= n; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
        // System.out.println("");
    }
}
